    out.push_str("  | LifecycleEvent\n");
    out.push_str("  | LogEvent\n");
    out.push_str("  | PlayerEvent;\n\n");

    out.push_str("/** An event with a `type` field that doesn't match a known type. Forwarded unchanged. */\n");
    out.push_str("export type UnknownEvent = OrchestratorStamp & {\n");
    out.push_str("  type: string;\n");
    out.push_str("  [key: string]: unknown;\n");
    out.push_str("};\n\n");

    out.push_str("export type AnyEvent = PipelineEvent | UnknownEvent;\n\n");

    // Type discrimination
    out.push_str("// ---- Type discrimination ----\n\n");
    out.push_str("const KNOWN_TYPES = new Set([\n");
    for ty in categories::ALL_EVENT_TYPES {
        out.push_str(&format!("  \"{}\",\n", ty));
    }
    out.push_str("]);\n\n");

    out.push_str("export function isKnownEventType(type: string): boolean {\n");
    out.push_str("  return KNOWN_TYPES.has(type);\n");
    out.push_str("}\n\n");

    // Serialization
    out.push_str("// ---- Serialization ----\n\n");
    out.push_str("export function parseEvent(json: string): AnyEvent {\n");
    out.push_str("  const obj = JSON.parse(json);\n");
    out.push_str("  if (!obj || typeof obj !== \"object\" || typeof obj.type !== \"string\") {\n");
    out.push_str("    throw new Error(\"Invalid event: missing 'type' field\");\n");
    out.push_str("  }\n");
    out.push_str("  return obj as AnyEvent;\n");
    out.push_str("}\n\n");

    out.push_str("export function serializeEvent(event: AnyEvent): string {\n");
    out.push_str("  return JSON.stringify(event);\n");
    out.push_str("}\n\n");

